package com.example.lab7;

public class Posts {

        private String title;
        private String body;

        // generate Constructor
        public Posts(String title, String body) {
            this.title = title;
            this.body = body;
        }

        //Generate Getters
        public String getTitle() {
            return title;
        }

        public String getBody() {
            return body;
        }
    }


